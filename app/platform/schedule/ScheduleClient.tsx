/**
 * Month / Week / Day calendar with an "Anytime" all-day row, an
 * unscheduled-jobs drawer, and drag-to-(re)schedule. Mirrors Jobber's
 * schedule layout (build spec §6) without the map/route extras.
 *
 * Drag rules:
 *  - drop on an hour cell  → starts at that hour, keeps the job's previous
 *    duration (default 1h), clears "anytime"
 *  - drop on the Anytime row / a month day cell with no previous time
 *    → date-only ("anytime", anchored at noon like other date-only fields)
 *  - drop on a month day cell when the job had a time → keeps that time
 */

// Blocked-off time riding along in the DTO — the calendar shows per-day
// segments but the sheet edits the underlying block's full range.
export type BlockInfo = {
  id: string;
  userId: string | null; // null = everyone (company-wide)
  userName: string | null;
  title: string;
  startAt: string;
  endAt: string;
  allDay: boolean;
  canEdit: boolean;
};

export type ScheduleJobDTO = {
  id: string;
  kind: "job" | "appointment" | "block";
  jobNumber: number | null;
  title: string;
  status: string;
  apptType: string | null; // PHONE_CALL | VIDEO_CALL | IN_PERSON
  scheduledAt: string | null;
  scheduledEnd: string | null;
  scheduledAnytime: boolean;
  contactName: string;
  tentative?: boolean; // self-scheduled booking awaiting approval → dashed
  recurring?: boolean; // visit generated by a subscription's visit series
  block?: BlockInfo;
};

type View = "month" | "week" | "day";

